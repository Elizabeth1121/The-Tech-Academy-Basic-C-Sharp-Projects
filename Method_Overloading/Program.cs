using Method_Overloading;


// Create an instance of the Math_Operations class
Math_Operations math = new Math_Operations();

// Call the Add method and pass in an integer
Console.WriteLine(math.Add(5));

// Call the Add method and pass in a decimal
Console.WriteLine(math.Add(5.5m));

// Call the Add method and pass in a string
Console.WriteLine(math.Add("5"));